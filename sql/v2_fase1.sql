-- ── 5. Conferência ──────────────────────────────────────────────────────────
-- Compare os totais: prices deve ser a soma das 4 tabelas antigas
-- (menos eventuais duplicatas ignoradas pelo on conflict).

select 'flight_prices'        as tabela, count(*) from flight_prices
union all select 'flight_prices_auto',   count(*) from flight_prices_auto
union all select 'domestic_prices',      count(*) from domestic_prices
union all select 'domestic_prices_auto', count(*) from domestic_prices_auto
union all select 'prices (nova)',        count(*) from prices;

select t.name, t.period, p.source, count(*)
from prices p join trips t on t.id = p.trip_id
group by 1, 2, 3
order by 1, 2, 3;
