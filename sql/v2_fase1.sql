-- ── 4. Buscas rastreadas (config da automação — usada na Fase 2) ────────────

create table tracked_searches (
  id             bigint generated always as identity primary key,
  trip_id        bigint not null references trips(id) on delete cascade,
  api            text not null check (api in ('serpapi', 'talordata', 'rapidapi')),
  airline        text not null,            -- como salvar no banco (ex: 'Gol')
  airline_match  text not null,            -- substring p/ filtrar resposta (ex: 'GOL')
  origin         text not null,            -- sempre na direção da ida
  destination    text not null,
  trip_type      text not null default 'round_trip'
                 check (trip_type in ('outbound', 'return', 'round_trip')),
  max_stops      int  not null default 0,
  outbound_times text,                     -- ex: '4,11'  (ida de manhã)
  return_times   text,                     -- ex: '12,23' (volta tarde/noite)
  active         boolean not null default true,
  created_at     timestamptz default now()
);

alter table tracked_searches enable row level security;
create policy "searches public read"   on tracked_searches for select using (true);
create policy "searches auth write"    on tracked_searches for all to authenticated using (true) with check (true);
create policy "searches service write" on tracked_searches for all to service_role  using (true) with check (true);

