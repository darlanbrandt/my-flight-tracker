-- ── 1. Viagens ──────────────────────────────────────────────────────────────

create table trips (
  id         bigint generated by default as identity primary key,
  name       text not null,
  period     text not null,                -- ex: "Nov/2026"
  kind       text not null check (kind in ('internacional', 'domestica')),
  date_out   date not null,
  date_back  date not null,
  created_at timestamptz default now()
);

alter table trips enable row level security;
create policy "trips public read"   on trips for select using (true);
create policy "trips auth write"    on trips for all to authenticated using (true) with check (true);
create policy "trips service write" on trips for all to service_role  using (true) with check (true);

