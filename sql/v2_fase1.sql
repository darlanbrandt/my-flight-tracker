-- ── 2. Preços (única, substitui as 4 tabelas) ───────────────────────────────

create table prices (
  id          uuid primary key default gen_random_uuid(),
  trip_id     bigint not null references trips(id) on delete cascade,
  date        date not null,
  airline     text not null,               -- texto livre: qualquer companhia
  origin      text not null,
  destination text not null,
  trip_type   text not null default 'round_trip'
              check (trip_type in ('outbound', 'return', 'round_trip')),
  price_out   numeric(10,2),
  price_back  numeric(10,2),
  total       numeric(10,2) generated always as
              (coalesce(price_out, 0) + coalesce(price_back, 0)) stored,
  source      text not null default 'manual' check (source in ('manual', 'auto')),
  notes       text,
  created_at  timestamptz default now(),
  unique (trip_id, date, airline, origin, destination, trip_type, source)
);

create index prices_trip_date_idx on prices (trip_id, date);

alter table prices enable row level security;
create policy "prices public read"   on prices for select using (true);
create policy "prices auth write"    on prices for all to authenticated using (true) with check (true);
create policy "prices service write" on prices for all to service_role  using (true) with check (true);

